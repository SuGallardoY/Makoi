    private void ButtonActualizarActionPerformed(java.awt.event.ActionEvent evt) {
     
             String nombre_producto, tipo_producto, material, color;          
        int precio;
       int stock;

        nombre_producto = TextFieldNombreProducto.getText();
        if (nombre_producto.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese t√≠tulo", "Validaci√≥n", JOptionPane.WARNING_MESSAGE);
            TextFieldNombreProducto.requestFocus();
            return;
        }

       tipo_producto = TextFieldTipoProducto.getText();
        if (tipo_producto.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese autor", "Validaci√≥n", JOptionPane.WARNING_MESSAGE);
            TextFieldTipoProducto.requestFocus();
            return;
        }
        
          material = TextFieldMaterial.getText();
        if (material.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese autor", "Validaci√≥n", JOptionPane.WARNING_MESSAGE);
            TextFieldMaterial.requestFocus();
            return;
        }
        
           color = TextFieldColor.getText();
        if (color.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese autor", "Validaci√≥n", JOptionPane.WARNING_MESSAGE);
            TextFieldColor.requestFocus();
            return;
        }
     

        try {
            precio = Integer.parseInt(TextFieldPrecio.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Elprecio debe ser un n√∫mero", "Validaci√≥n", JOptionPane.WARNING_MESSAGE);
          TextFieldPrecio.requestFocus();
            return;
        }
        
                try {
            stock = Integer.parseInt(TextFieldStock.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Elprecio debe ser un n√∫mero", "Validaci√≥n", JOptionPane.WARNING_MESSAGE);
          TextFieldStock.requestFocus();
            return;
        }

       Producto producto = new Producto(id_producto, nombre_producto, tipo_producto, material, color, precio, stock);

        Controlador con= new Controlador();

        
        if (con.actualizar(producto)) {
            JOptionPane.showMessageDialog(this, "Se Actualizo producto  " + producto.getNombre_producto(), "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "No se actualiz√≥ producto  " + producto.getNombre_producto(), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }                                                


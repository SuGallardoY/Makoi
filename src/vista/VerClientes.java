    private void ButtonVerClientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonVerClientesActionPerformed
            int rut_cliente;
        String nombre_cliente, direccion_envio, comuna, productos_comprados; 

        Controlador con = new Controlador();
        DefaultTableModel modelo = (DefaultTableModel) TBClientes.getModel();

        try {
            rut_cliente= Integer.parseInt(TextFieldRutCliente.getText());
        } catch (Exception e) {
            rut_cliente= 0;
        }

        modelo.setRowCount(0);

        if (rut_cliente == 0) { // listsr todos
            List<Cliente> lista = con.MostrarTodosClientes();

            for (Cliente tmp : lista) {
                nombre_cliente = tmp.getNombre_cliente();
                direccion_envio = tmp.getDireccion_envio();
                comuna = tmp.getComuna();
               productos_comprados = tmp.getProductos_comprados();
             
                

                modelo.addRow(new Object[]{nombre_cliente, direccion_envio, comuna, productos_comprados});
            }

        } else { //consulta por id
            Cliente cliente = con.buscarPorRut(rut_cliente);
            nombre_cliente = cliente.getNombre_cliente();
           direccion_envio = cliente.getDireccion_envio();
           comuna = cliente.getComuna();
            productos_comprados = cliente.getProductos_comprados();
     

            modelo.addRow(new Object[]{nombre_cliente, direccion_envio, comuna, productos_comprados});

        }
    }//GEN-LAST:event_ButtonVerClientesActionPerformed


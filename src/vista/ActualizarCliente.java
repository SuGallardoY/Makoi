    private void ButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonBuscarActionPerformed
   String nombreCliente = TextFieldNombreCliente.getText();
        Controlador con = new Controlador();
        Cliente cliente = con.buscarPorNombreCliente(nombreCliente);
        if(cliente.getRut_cliente() != 0){            
            TextFieldNombre.setText(cliente.getNombre_cliente());
            TextFieldDireccionEnvio.setText(cliente.getDireccion_envio());
            TextFieldComuna.setText(cliente.getComuna());
            TextAreaProductosComprados.setText(cliente.getProductos_comprados());
            rut_cliente = cliente.getRut_cliente();      
        }else{
            JOptionPane.showMessageDialog(null, "Cliente  no encontrado");
        }
    }//GEN-LAST:event_ButtonBuscarActionPerformed


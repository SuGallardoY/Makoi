    private void ButtonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonActualizarActionPerformed
        String nombre_cliente, direccion_envio, comuna, productos_comprados;           


        nombre_cliente = TextFieldNombre.getText();
        if (nombre_cliente.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese nombre y apellido del cliente", "Validación", JOptionPane.WARNING_MESSAGE);
            TextFieldNombre.requestFocus();
            return;
        }

        direccion_envio = TextFieldDireccionEnvio.getText();
        if ( direccion_envio.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese dirección de envío", "Validación", JOptionPane.WARNING_MESSAGE);
           TextFieldDireccionEnvio.requestFocus();
            return;
        }
        
         comuna= TextFieldComuna.getText();
        if ( comuna.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese dirección de envío", "Validación", JOptionPane.WARNING_MESSAGE);
           TextFieldComuna.requestFocus();
            return;
        }
        
          productos_comprados = TextAreaProductosComprados.getText();
        if ( productos_comprados.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese dirección de envío", "Validación", JOptionPane.WARNING_MESSAGE);
           TextAreaProductosComprados.requestFocus();
            return;
        }


        Cliente cliente = new Cliente(rut_cliente, nombre_cliente, direccion_envio, comuna, productos_comprados);

        Controlador con = new Controlador();

        
        if (con.actualizarCliente(cliente)) {
            JOptionPane.showMessageDialog(this, "Se Actualizo cliente  " + cliente.getNombre_cliente(), "Información", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "No se actualizó cliente  " + cliente.getNombre_cliente(), "Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_ButtonActualizarActionPerformed

